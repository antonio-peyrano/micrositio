/**
 * Este archivo de script contiene los comandos de ejecución para la interfaz de gestión del
 * catálogo de entidades en el sistema.
 */

function guardarEntidad(url,parametro){
	/*
	 * Esta función valida que los datos para ser almacenados en el registro sean correctos.
	 */
	var error= 0;
	
	if(document.getElementById("Entidad").value.toString() == "")
		{
			//En caso de no ocurrir un error de validación, se asigna el valor de paso.
			error= error+1;
			}
	
	if(document.getElementById("idTEntidad").value.toString() == "-1")
		{
			//En caso de no ocurrir un error de validación, se asigna el valor de paso.
			error= error+1;
			}
	
	if(error > 0)
		{
			/*
			 * En caso de ocurrir un error de validación, se notifica al usuario.
			 */
        	bootbox.alert("Existen campos pendientes por completar");
			}
	else
		{
			/*
			 * En caso que la validación de campos sea satisfactoria.
			 */
			cargar(url,parametro,'sandbox');
			}
	}
	
function habEntidad(){
	/*
	 * Esta función habilita los controles del formulario de colonia.
	 */

	document.getElementById('Entidad').disabled = false;
	document.getElementById('idTEntidad').disabled = false;
	document.getElementById('ent_Guardar').style.display="block";
	document.getElementById('ent_Borrar').style.display="none";
	document.getElementById('ent_Editar').style.display="none";	
	}

/*
 * El presente segmento de codigo evalua la accion de click sobre cualquier elemento con el id buscar_#
 * para ejecutar la acción de actualización sobre el registro de una rejilla de datos.
 */
$(document).ready(function() {
    $("div").click(function(e){
    	e.stopPropagation();
    	if(e.target.id.substring(0,10) == "ent_buscar")
    		{
    			//Si el usuario confirma su solicitud de borrar el registro seleccionado.
				document.getElementById('pgentidad').value = document.getElementById('nomentidad').value.toString();
				document.getElementById('pgidtentidad').value = document.getElementById('nomidtentidad').value.toString();    		
    			cargar('./php/frontend/entidades/catEntidades.php','?nomentidad='+document.getElementById('nomentidad').value.toString()+'&nomidtentidad='+document.getElementById('nomidtentidad').value.toString(),'busRes');
    			}
    });                 
});

